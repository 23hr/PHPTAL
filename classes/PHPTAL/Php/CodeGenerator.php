<?php

require_once 'PHPTAL/Php/Node.php';
require_once 'PHPTAL/Php/State.php';
require_once 'PHPTAL/Php/CodeWriter.php';

class PHPTAL_Php_CodeGenerator
{
    public function __construct($sourcePath)
    {
        $this->_functionName = 'tpl_'.PHPTAL_VERSION.md5($sourcePath);
        $this->_sourceFile = $sourcePath;
        $this->_state = new PHPTAL_Php_State();
        $this->_generator = new PHPTAL_Php_CodeWriter($this->_state);
    }

    public function setOutputMode($mode)
    { 
        $this->_state->setOutputMode($mode);
    }
    
    public function setEncoding($enc)
    { 
        $this->_state->setEncoding($enc);
    }

    public function generate(PHPTAL_NodeTree $tree)
    {
        $treeGen = new PHPTAL_Php_NodeTree($this->_generator, $tree);

        $header = sprintf('Generated by PHPTAL from %s', $this->_sourceFile);
        $this->_generator->doFunction($this->_functionName, '$tpl, $ctx');
        $this->_generator->doComment($header);
        $this->_generator->setFunctionPrefix($this->_functionName . "_");
        $this->_generator->pushCode('ob_start()');
        $treeGen->generate();
        $this->_generator->pushCode('$_result_ = ob_get_contents()');
        $this->_generator->pushCode('ob_end_clean()');
        $this->_generator->pushCode('return $_result_');
        $this->_generator->doEnd();
    }

    public function getResult()
    {
        return $this->_generator->getResult();
    }

    private $_functionName;
    private $_sourceFile;
    private $_generator;
    private $_state;
}

?>
